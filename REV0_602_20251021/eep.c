U8 EEP_Block_Check( U16 addr )
{
	U8	Block = 0;

	if( ( EEP_PREFIX_1 < addr ) && ( addr < EEP_FLAME_TYPE ) )
	{
		Block = EEP_BLOCK_1;
	}
	else if( ( EEP_FLAME_TYPE <= addr ) && ( addr < EEP_RATIO_RESERVED ) )
	{
		Block = EEP_BLOCK_2;
	}
	else if( ( EEP_RATIO_RESERVED <= addr ) && ( addr < TRIPLE_IR_REL_RATIO_530_SIZE_1 ) )
	{
		Block = EEP_RATIO_395;
	}
	else if( ( TRIPLE_IR_REL_RATIO_530_SIZE_1 <= addr ) && ( addr < TRIPLE_IR_REL_RATIO_MAX ) )
	{
		Block = EEP_RATIO_530;
	}
	else
	{
		EepErrorFlag = 1;
	}
	
	//printf("\r\n add = %d, Block = %d, Err-%d", addr, Block, EepErrorFlag);
	
	return Block;
}


