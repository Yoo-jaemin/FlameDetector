void EEP_SysFormat( U8 Eep_Block )
{
	U16		i;
	
	P24 = 24.f;
	
	switch(Eep_Block)
	{
		case EEP_BLOCK_1:
			for( i=0; i<EEP_FLAME_TYPE; i++ ){
				if( ( i == EEP_SERIAL_NO_L ) || ( i == EEP_SERIAL_NO_H ) ||
				( i == EEP_UV_DET_LMT ) || ( i == EEP_CAL_BUF_UV_CPS ) ||
				(( i >= EEP_CAL_BUF_IR430_RESPONSIVITY ) && ( i <= EEP_CAL_BUF_IR530_07 )) )
				//if( ( i == EEP_SERIAL_NO_L ) || ( i == EEP_SERIAL_NO_H ) )
				{
					epr_sm[i] = EEP_Rd(i);
					Delay_ms( 1 );
				}
				else {
					epr_sm[i] = pgm_read_word( &EEP_SYS_DEFAULT[i] );
					EEP_Wr( i, epr_sm[i] );
				}
			}
			break; 	
		case EEP_BLOCK_2:
			for( i=EEP_FLAME_TYPE; i<=EEP_MAX; i++ ){
				if( i == EEP_FLAME_TYPE)
				{
					epr_sm[i] = EEP_Rd(i);
					Delay_ms( 1 );
				}
				else {
					epr_sm[i] = pgm_read_word( &EEP_SYS_DEFAULT[i] );
					EEP_Wr( i, epr_sm[i] );
				}
			}
			break;
		case EEP_RATIO_395:
			for (i = EEP_RATIO_RESERVED; i <=TRIPLE_IR_REL_RATIO_395_SIZE_999; i++)
			{
				EEP_Wr(i, pgm_read_word( &EEP_SYS_DEFAULT[i] ));
			}
			break;
			
		case EEP_RATIO_530:
			for (i = TRIPLE_IR_REL_RATIO_530_SIZE_1; i <=TRIPLE_IR_REL_RATIO_MAX; i++)
			{
				EEP_Wr(i, pgm_read_word( &EEP_SYS_DEFAULT[i] ));
			}
			break;
		default: 
			EepErrorFlag = 1;
			break;
	}
	
	EEP_CRC_Wr( Eep_Block );
}
